			<h3><strong>Super Store Finder</strong> - Easy to use Google Maps API Store Finder</h3>
<strong>Super Store Finder</strong> is a fully featured PHP Application integrated with Google Maps API v3 that allows customers to locate your stores easily. Packed with Geo Location, your customers will be able to view a list of nearby stores / outlets around them sorted by distance. 

Setup is really easy and quick (<a href="http://superstorefinder.net/products/superstorefinder/docs" target="new">Refer setup guide here</a>). Deploying the store finder to any website gets even easier when you can embed it to any web page or CMS (Joomla/Wordpress/Drupal/etc) by simply copy and paste the embed code. The mobile friendly user interface works on various platform such as PC, laptops, smart phones and other mobile devices.

View the <a href="http://superstorefinder.net/products/superstorefinder" target="new">Store Finder Demo</a> and <a href="http://superstorefinder.net/products/superstorefinder/admin" target="new">Administrator's Demo</a> here 

View the <a href="http://superstorefinder.net/products/superstorefinder/index_geoip.php">Store Demo that detects where you are</a>

